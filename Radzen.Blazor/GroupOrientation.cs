namespace Radzen;

/// <summary>
/// Specifies the orientation of resource grouping in scheduling views.
/// </summary>
public enum GroupOrientation
{
    /// <summary>
    /// Resources are displayed vertically (as rows), and time runs horizontally.
    /// </summary>
    Vertical,

    /// <summary>
    /// Resources are displayed horizontally (as columns), and time/dates run horizontally under each resource.
    /// </summary>
    Horizontal
}
